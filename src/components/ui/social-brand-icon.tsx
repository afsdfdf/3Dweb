export function normalizeSocialBrandPlatform(platform?: null | string) {
  const normalized = typeof platform === 'string' ? platform.trim().toLowerCase() : ''

  return normalized && socialBrandIcons[normalized] ? normalized : 'website'
}

export function SocialBrandIcon({ className, platform, style, title }: SocialBrandIconProps) {
  const normalizedPlatform = normalizeSocialBrandPlatform(platform)
  const icon = socialBrandIcons[normalizedPlatform]

  return (
    <svg
      aria-hidden={title ? undefined : true}
      className={className}
      fill="currentColor"
      focusable="false"
      role={title ? 'img' : undefined}
      style={style}
      viewBox={icon.viewBox}
      xmlns="http://www.w3.org/2000/svg"
    >
      {title ? <title>{title}</title> : null}
      {icon.paths.map((path) => (
        <path d={path} key={path} />
      ))}
    </svg>
  )
}
